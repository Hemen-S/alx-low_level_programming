/**
 * _strspn - ditactes segment that contains only bytes
 * @s: char pointer
 * @accept: char pointer
 * Return: *s
 */

unsigned int _strspn(char *s, char *accept);
{
    int i;
    while (*s)
    {
        if (s[i] = accept[i])
        {
            return(s);
        }
    }
}